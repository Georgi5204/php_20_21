<?php  

// Да се отпечата таблица с $m реда и $n колони като използвате цикъл for. 
//Във всяко
// поле пишем стойностите (ред, колона). $m и $n се въвеждат от потребителя чрез
// форма. 

$rows = 5;
$cols = 6;

?>
<table border="1">
	<?php 
	for( $i = 1; $i <= $rows; $i++ ){
		echo "<tr>";
		for( $j = 1; $j <= $cols; $j++ ){
			echo "<td>$i - $j</td>";
		}
		echo "</tr>";
	}
	?>
</table>